BASE = "https://smithjacki-hermes.hf.space"
TIMEOUT = 15

def api_get(path, params=None):
    url = BASE + path
    if params:
        url += "?" + urllib.parse.urlencode(params)
    try:
        req = urllib.request.Request(url, headers={"User-Agent": "CakeGame-Checker/1.0"})
        with urllib.request.urlopen(req, timeout=TIMEOUT) as resp:
            return json.loads(resp.read().decode())
    except Exception as e:
        return {"error": str(e)}

def api_cmd(msg, uid="checker"):
    return api_get("/api/cmd", {"msg": msg, "uid": uid})

def api_batch(cmds, uid="checker"):
    return api_get("/api/batch", {"cmds": "|".join(cmds), "uid": uid})

