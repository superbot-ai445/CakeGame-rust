def main():
    print(f"=== CakeGame 定时检测 {time.strftime('%Y-%m-%d %H:%M:%S')} ===\n")

    # 基础测试
    print("--- 基础功能测试 ---")
    basic = run_tests()
    print(f"通过: {basic['pass']}, 失败: {basic['fail']}, 错误: {basic.get('error', 0)}")

    for d in basic["details"]:
        if d["status"] != "PASS":
            print(f"  ❌ {d['name']}: {d.get('error', d.get('got', ''))[:100]}")

    # 高级测试
    print("\n--- 高级功能测试 ---")
    adv = run_advanced_tests()
    print(f"通过: {adv['pass']}, 失败: {adv['fail']}")

    for d in adv["details"]:
        if d["status"] != "PASS":
            print(f"  ❌ {d['name']}: {d.get('got', '')[:100]}")

    # 总结
    total_pass = basic["pass"] + adv["pass"]
    total_fail = basic["fail"] + adv["fail"] + basic.get("error", 0)
    total = total_pass + total_fail
    rate = (total_pass / total * 100) if total > 0 else 0

    print(f"\n总计: {total_pass}/{total} 通过 ({rate:.0f}%)")

    # 输出失败项供 agent 分析
    failures = []
    for d in basic["details"] + adv["details"]:
        if d["status"] != "PASS":
            failures.append(d)

    if failures:
        print(f"\n需要修复的 {len(failures)} 项:")
        for f in failures:
            print(f"  - {f['name']}: {f.get('error', f.get('got', ''))[:150]}")

    # 返回给 cron agent 的摘要
    summary = {
        "time": time.strftime('%Y-%m-%d %H:%M:%S'),
        "basic_pass": basic["pass"],
        "basic_fail": basic["fail"],
        "adv_pass": adv["pass"],
        "adv_fail": adv["fail"],
        "total_pass": total_pass,
        "total_fail": total_fail,
        "rate": f"{rate:.0f}%",
        "failures": [f["name"] for f in failures],
    }
    print(f"\n--- JSON ---")
    print(json.dumps(summary, ensure_ascii=False))

if __name__ == "__main__":
    main()
